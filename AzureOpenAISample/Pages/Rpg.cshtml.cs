## アイテム
* アイテムには、各種武器、各種防具、各種薬草などがあります。
* 店でアイテムを売ることで、ゴールドを得ることができます。良いアイテムであればあるほど、値段は高くなります。
## バトルシステム
* ゲーム内にはモンスターが登場します。
* モンスターやラスボスはドラゴンクエストのように多種多様です。
* 戦闘中はモンスターの名前が表示されます。
* 経験値が上がると、経験値が増加します。
* 経験値が貯まると、レベルが1つずつ上がっていきます。
